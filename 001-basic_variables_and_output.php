<?php
// all PHP must start with the tag above

// lines that start like this are comments

/*
	This is also a comment, and is used for making comments
	that are more like a paragraph
*/

// Variables hold data. Here we make a variable called $a
$a; // this is a variable called $a

// $a doesn't have any data in it right now.

$a = 5 * 6;

// echo outputs words to the command prompt.
// \n is a special character called a "new line"
echo "$a\n";

// This creates the variable $b, and sets it to 2.6
$b = 2.6;


echo $a * $b;

?>